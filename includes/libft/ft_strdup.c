#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*tab;
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (s[len])
		len++;
	tab = malloc(len * sizeof(char) + 1);
	if (!tab)
		return (NULL);
	while (s[i])
	{
		tab[i] = s[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
